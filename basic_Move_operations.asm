ORG 00H
MOV R1, #03H; stores hexadecimal value of 3 at R1 register
MOV R2, #42H; stores hexadecimal value of 42 at R2 register
MOV R3, #17H; stores hexadecimal value of 17 at R3 register
END ;this is very basic code, you may experiment with different values of numbers as well as different registers in the range of R0-R7, A, and B.